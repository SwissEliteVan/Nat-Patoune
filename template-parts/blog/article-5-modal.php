                    <p>Indiquez clairement les quantités et la fréquence. Si votre chat a tendance à se jeter sur la nourriture, mieux vaut fractionner les repas (et éviter les changements brutaux pendant votre absence).</p>
                    
                    <h2 class="font-title font-bold text-2xl text-brand-text mt-8 mb-4">La litière : confort et prévention</h2>
                    <p>Une litière propre limite le stress et les "accidents". Prévoyez une réserve suffisante, un sac poubelle dédié et, si possible, une seconde litière quand votre chat est sensible.</p>
                    
