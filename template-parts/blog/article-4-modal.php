                    <p class="font-medium text-lg text-brand-text mb-6">Une garde à domicile réussie, c'est surtout une bonne préparation. Avec une checklist claire, vous sécurisez le confort de votre chat, vous facilitez le travail du cat-sitter, et vous partez l'esprit plus léger. Voici les points essentiels à vérifier avant votre départ.</p>
                    
                    <h2 class="font-title font-bold text-2xl text-brand-text mt-8 mb-4">1) Rassembler les informations clés</h2>
                    <p>Notez noir sur blanc les habitudes de votre chat : type de nourriture, quantités, horaires, préférences de jeu, zones où il aime se cacher, et tout comportement à connaître (timidité, tendance à griffer, etc.).</p>
                    
                    <h2 class="font-title font-bold text-2xl text-brand-text mt-8 mb-4">2) Préparer une "zone cat-sitting"</h2>
                    <p>Regroupez au même endroit les éléments utiles : croquettes/pâtées, sacs de litière, sacs poubelle, friandises, médicaments, carnet vétérinaire. Moins on cherche, plus la visite est efficace et sereine.</p>
                    
                    <h2 class="font-title font-bold text-2xl text-brand-text mt-8 mb-4">3) Anticiper la sécurité du domicile</h2>
                    <h3 class="font-bold text-xl text-brand-text mt-6 mb-2">Fenêtres, balcons, objets dangereux</h3>
                    <p>Vérifiez la sécurité des accès (fenêtres oscillo-battantes, balcons, filets). Rangez les produits ménagers, plantes toxiques et petits objets fragiles.</p>
                    
                    <h2 class="font-title font-bold text-2xl text-brand-text mt-8 mb-4">4) Lister les contacts utiles</h2>
                    <p>Préparez : numéro du vétérinaire habituel, une personne de confiance en cas d'urgence, et les consignes d'accès (interphone, boîte à clés si besoin).</p>
                    
